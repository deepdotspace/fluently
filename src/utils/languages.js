export function getLanguageByCode(code) {
  return LANGUAGES.find(lang => lang.code === code) || LANGUAGES[0];
}

export function getLanguageName(code) {
  const lang = getLanguageByCode(code);
  return lang ? `${lang.flag} ${lang.name}` : code;
}

